def addition(first_number, second_number):
    result = first_number + second_number
    return result


def subtraction(first_number, second_number):
    result = first_number - second_number
    return result


def multiply(first_number, second_number):
    result = first_number * second_number
    return result


def divide(first_number, second_number):
    result = first_number / second_number
    return result
